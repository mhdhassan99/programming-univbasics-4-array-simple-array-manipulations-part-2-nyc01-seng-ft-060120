def using_concat(array1, array2)
  array1.concat(array2)
end

def using_insert(array, element)
  array.insert(element)
end


def using_uniq(array)
  array.uniq()
end

def using_flatten(array)
  array.flatten()
end


def using_delete(array, String)
  i = 0
  while i < array.length do 
    if array[i] == string
      array.delete(i)
    end
    i += 1
  end
end


